type chunkReader struct {
	data            string
	numBytesPerRead int
	pos             int
}

func (r *chunkReader) Read(p []byte) (n int, err error) {
	if r.pos >= len(r.data) {
		return 0, io.EOF
	}

	endPos := min(r.pos+r.numBytesPerRead, len(r.data))
	n = copy(p, r.data[r.pos:endPos])
	r.pos += n
	return n, nil
}

